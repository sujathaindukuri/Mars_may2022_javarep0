package JavaPrograms;

public class CalculateDays {
	public static void main(String args[])
	{
		System.out.println("hello ");
		for( int i=1;i<=3;i++) {
			for( int j=1;j<=i;j++)
			{
				System.out.print(j);
		}
			System.out.println();
	}
		int k=1;
		for( int i=1;i<=3;i++) {
			for( int j=1;j<=i;j++)
			{
				System.out.print(k);
				k=k+1;
		}
			System.out.println();
	}
}
}